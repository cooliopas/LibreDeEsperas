package com.uxor.turnos.view.dto;

import java.math.BigDecimal;

public class BranchsQtyDto {
	
	private BigDecimal clientTypeId;
	private String clientTypeDesc;
	private BigDecimal branchQty;
	private Boolean myTown;
	
	public BranchsQtyDto(){
	}

	public BranchsQtyDto(BigDecimal clientTypeId, String clientTypeDesc, BigDecimal branchQty, Boolean myTown){
		setClientTypeId(clientTypeId);
		setClientTypeDesc(clientTypeDesc);
		setBranchQty(branchQty);
		setMyTown(myTown);
	}
	
	public BigDecimal getClientTypeId() {
		return clientTypeId;
	}
	
	public void setClientTypeId(BigDecimal clientTypeId) {
		this.clientTypeId = clientTypeId;
	}
	
	public String getClientTypeDesc() {
		return clientTypeDesc;
	}
	
	public void setClientTypeDesc(String clientTypeDesc) {
		this.clientTypeDesc = clientTypeDesc;
	}
	
	public BigDecimal getBranchQty() {
		return branchQty;
	}

	public void setBranchQty(BigDecimal branchQty) {
		this.branchQty = branchQty;
	}

	public Boolean getMyTown() {
		return myTown;
	}

	public void setMyTown(Boolean myTown) {
		this.myTown = myTown;
	}

}
