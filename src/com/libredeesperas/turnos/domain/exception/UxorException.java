package com.uxor.turnos.domain.exception;


public class UxorException extends Exception {
	
	private static final long serialVersionUID = 1L;

	/**
	 * Constructor con mensaje de error.
	 * @param msg El mensaje de error asociado con la excepción
	 */

	public UxorException(String msg) {
		super(msg);
	}
	
	/**
	 * Constructor con mensjae de error y causa raíz.
	 * 
	 * @param msg el mensaje de error asociado con la excepción.
	 * @param cause la causa raíz asociado con la excepción.
	 */
	public UxorException(String msg, Throwable cause) {
		super(msg, cause);
	}

}
