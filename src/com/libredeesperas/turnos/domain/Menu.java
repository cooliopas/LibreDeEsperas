package com.uxor.turnos.domain;


public class Menu {
	
	private int id;
	private String codigo;
	private String descripicion; //Error en el name, refactorizar :P
	private String logo;
	private String style;

	public Menu(){
	}
	
	
	public Menu(int id, String codigo, String descripcion, String logo, String style){
		
		setId(id);
		setCodigo(codigo);
		setDescripicion(descripcion);
		this.logo=logo;
		this.style=style;


	}


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getCodigo() {
		return codigo;
	}


	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}


	public String getDescripicion() {
		return descripicion;
	}


	public void setDescripicion(String descripicion) {
		this.descripicion = descripicion;
	}
	
	public String getLogo() {
		return logo;
	}


	public void setLogo(String logo) {
		this.logo = logo;
	}
	
	public String getStyle() {
		return style;
	}


	public void setStyle(String style) {
		this.style = style;
	}

}
